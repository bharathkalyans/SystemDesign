package snakeandladder;


public class Jumper {
    private int startPoint, endPoint;

    public int getStartPoint() {
        return startPoint;
    }

    public int getEndPoint() {
        return endPoint;
    }

    public Jumper(int _startPoint, int _endPoint) {
        this.startPoint = _startPoint;
        this.endPoint = _endPoint;
    }
}
